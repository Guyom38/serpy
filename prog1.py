
# Saisie de l'utilisateur
choix = input("Donnes moi un chiffre de 1 a 9 ?")

# Conditions
if choix == 1:
    print("T'es pas beau")

elif choix == 2:
    print("Tu sents pas bon")

elif choix == 3:
    print("Ta tête est énorme")

elif choix == 4:
    print("Tes une chèvre")

else:
    print("J'ai plus d'idée")